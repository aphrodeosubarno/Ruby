# * Membuat object

# ! Setelah class didefinisikan, maka kita dapat membuat object dari class tersebut menggunakan bentuk umum berikut:
# variable = ClassName.new()

# contoh kode program
class PersegiPanjang
    # class body
end

# membuat object baru
obj = PersegiPanjang.new()

# ! Note: method new() tidak dimiliki oleh class Integer dan Float. Selain itu, seluruh class yang didefinisikan di Ruby memiliki method new().