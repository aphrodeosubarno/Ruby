# ! Ruby adalah bahasa yang mendukung pemrograman berorientasi object / oop (object oriented programming).
# ! Dalam Ruby, semua nilai yang digunakan oleh program merupakan object (mirip seperti javascript), seperti 9 (object dari class Integer), 'text' (object dari class String), dan masih banyak lainnya.

# untuk lebih jelasnya, perhatikan contoh berikut
puts 9.class # Integer

# ! Pada kode diatas, menunjukkan bahwa 9 disebut sebagai object atau hasil instansiasi dari class Integer.
# ! Dan Integer disebut sebagai class.

# ! Class didefinisikan sebagai blueprint yang digunakan untuk menggambarkan atribute dan perilaku dari suatu object tertentu.
# ! Contohnya misalnya adalah class Mobil, yang menggambarkan atribut mobil seperti warna, tahun keluaran dan perilaku dari mobil tersebut, seperti maju, mundur, belok kiri atau kanan.
# ! Dalam class, atribute dinyatakan dalam bentuk variable / instance variable dan perilaku dinyatakan dalam bentuk method / function dalam class.
# ! Setiap class akan memiliki daftar atribute dan method yang berbeda-beda, tergantuk dari sifat object yang digambarkan, seperti contoh dalam makhluk hidup, ada class HewanMamalia yang akan memiliki atribute dan method yang berbeda dengan class HewanBertelur.
