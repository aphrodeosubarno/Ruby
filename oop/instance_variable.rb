# * Instance Variable

# ! Seperti yang sudah dijelaskan pada pembelajaran sebelumnya, instance variable atau atribute merupakan sebuah variable yang didefinisikan didalam suatu class yang dimana penamaannya diawali dengan tanda at (@).

# contoh kode program
class Segitiga
    # mendefinisikan atribute / instance variable
    @alas = 0
    @tinggi = 0
end