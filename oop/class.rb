# * Mendefinisikan Class

# ! Dalam Ruby, sebuah class didefinisikan dengan kata kunci atau keyword `class`.
# ! Bentuk umumnya adalah sebagai berikut:
=begin
    class ClassName
        # class body
    end
=end
# ! Penting untuk diperhatikan, bahwa dalam Ruby, nama class harus diawali dengan huruf kapital.

# contoh kode program
class PersegiPanjang
  # class body
end
