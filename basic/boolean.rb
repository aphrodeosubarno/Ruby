# ! Boolean merupakan tipe data yang hanya memiliki dua nilai, yaitu true dan false.
# ! Dalam Ruby, true merupakan hasil instansiasi dari class TrueClass, sedangkan false merupakan hasil instansiasi dari class FalseClass.

# contoh
benar = true
salah = false

puts benar
puts salah

puts benar.class
puts salah.class
