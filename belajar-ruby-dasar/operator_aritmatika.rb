# ! Operator aritmatika adalah operator yang digunakan untuk melakukan operasi matematika.

# ! Macam-macam operator aritmatika
#* (+) => penjumlahan
#* (-) => pengurangan
#* (*) => perkalian
#* (/) => pembagian
#* (%) => modulus / sisa bagi
#* (**) => eksponen / perpangkatan

# contoh
a = 2
b = 4

puts b + a
puts b - a
puts b * a
puts b / a
puts b % a
puts b ** a

# ! Note: semua operasi aritmatika pada Ruby, jika angka yang dilakukan operasi merupakan angka Integer / bilangan bulat, maka hasil dari operasi tersebut akan menghasilkan bilangan bulat juga, termasuk dengan pembagian. Sedangkan jika angka yang dilakukan operasi merupakan angka Float / bilangan rill, maka hasil dari operasi tersebut akan menghasilkan bilangan rill juga. 