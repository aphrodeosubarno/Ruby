# ! Sama seperti C, Ruby mendukung adanya karakter escape sequence yang dapat disisipkan ke dalam string.

# ! Berikut ini adalah daftar karakter escape sequence yang didukung oleh Ruby:
# * \n: karakter newline
# * \r: karakter carriage return
# * \t: karakter tab
# * \v: karakter vertical tab
# * \b: karakter backspace
# * \' atau \": karakter single quote atau double quote
# * \\: karakter backslash

puts "Hello\nRuby"
puts "=" * 10
puts "Hello\rRuby"
puts "=" * 10
puts "Hello\tRuby"

