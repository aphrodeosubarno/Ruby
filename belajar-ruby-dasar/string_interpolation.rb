# ! String interpolation merupakan suatu cara menyisipkan variable atau expression apapun ke dalam string.
# ! String interpolation dapat dilakukan dengan menggunakan tanda pagar diikuti dengan kurung kurawal (`#{variable / expression}`).

# contoh jika menggunakan variable:
nama = "Deo"
umur = 18
puts "Halo, nama saya #{nama}, umur saya #{umur} tahun."

# contoh jika menggunakan expression:
a = 5
b = 10
puts "Hasil penjumlahan dari #{a} + #{b} adalah #{a + b}."